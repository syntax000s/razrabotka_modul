def triangle(weidgh,footing):
    """ s треугольник по основанию и высоте"""
    return f"s= {1/2*weidgh*footing}"


def rectangle(a,b):
    """ s прямоугольник"""
    return f"s= {a*b}p= {a*2+b*2}"

def circle(r):
    """ s круг по радиусу"""
    return f"s= {3.14*r*r}p= {2*3.14*r}"


